package com.fitness;

public class Landmark {
    public double x;
    public double y;
    public double z;
    public double visibility;

    public Landmark() {}

    public Landmark(double x, double y, double z, double visibility) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.visibility = visibility;
    }
}
